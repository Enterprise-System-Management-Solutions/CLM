AS 
SELECT CUSTOMER_ID, MONTHLY_DATA_PACK_BY_COUNT, MONTHLY_DATA_VOLUME_USED, 
MONTHLY_PACK_BY_COUNT, MONTHLY_RECHARGE_COUNT, MONTHLY_RECHARGE_AMOUNT, MONTHLY_MO_CALL, 
MONTHLY_MT_CALL, MONTHLY_VAS_REVENUE, MONTHLY_SMS_SEND_COUNT, MONTHLY_AVG_REVENUE, 
